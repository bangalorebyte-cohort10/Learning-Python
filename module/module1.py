
# Module defined for cohort 9

import math

def addition(a,b):
	return a+b

def subtraction(a,b):
	return a-b

def multiplication(a,b):
	return a*b

def division(a,b):
	return a/b

def modulo(a,b):
	return a%b

def squareroot(a):
	return math.sqrt(a)	

def power(a,b):
	return a**b

def power2(a,b):
	return a**b*2

